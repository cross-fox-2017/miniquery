const SweetSelector = {
  select: function (select) {
    if (select[0] === '#') {
      let id = select.substr(1)
      return document.getElementById(id)
    } else if (select[0] === '.') {
      let id = select.substr(1)
      return document.getElementsByClassName(id)
    } else {
      return document.getElementsByTagName(select)
    }
  }
}

