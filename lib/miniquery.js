var SweetSelector = {
  select: function(param) {
    if(param[0] == '#') {
      return document.getElementById(param.slice(1));
    } else if (param[0] == '.') {
      return document.getElementsByClassName(param.slice(1));
    } else {
      return document.getElementsByTagName(param);
    }
  }
}

