const SweetSelector = {
  select : function(selector){
    if (selector[0] === "#") {
      return document.getElementById(selector.slice(1))
    }
    else if (selector[0] === ".") {
      return document.getElementsByClassName(selector.slice(1))
    }
    else {
      return document.getElementsByTagName(selector)
    }
  }
}

